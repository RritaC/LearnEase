                    School Bags
                </a>
            </h3>
            <p class="mt-5 line-clamp-3 text-sm leading-6 text-gray-600">Heavy school bags have become a significant
                concern, impacting the health and well-being of students. Studies show that the average school bag
                weighs between 10-15% of a student's body weight, often exceeding the recommended safe limit. This
                excessive weight can lead to various physical issues, including back and shoulder pain, poor posture,
                and musculoskeletal problems. Young students, whose bodies are still developing, are particularly
                vulnerable to these adverse effects. Carrying heavy bags daily can also cause fatigue and reduce overall
                energy levels, negatively affecting academic performance and participation in extracurricular
                activities.
            </p>
